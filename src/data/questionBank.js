// Export all curated questions directly (no auto-generated placeholder questions)
export const questionBank = [...baseQuestionBank];

// Helper functions to filter questions
export function getQuestionsByTopic(topicId) {
  return questionBank.filter(q => q.topic === topicId);
}

export function getQuestionsByChapter(chapterId) {
  return questionBank.filter(q => q.chapter === chapterId);
}

export function getQuestionsBySubject(subjectId) {
  return questionBank.filter(q => q.subject === subjectId);
}

export function getQuestionsByFormula(formulaId) {
  return questionBank.filter(q => q.formulas?.includes(formulaId));
}

export function getQuestionsByDifficulty(difficulty) {
  return questionBank.filter(q => q.difficulty === difficulty);
}

export function getQuestionsByIds(ids) {
  return questionBank.filter(q => ids.includes(q.id));
}

export function getRandomQuestions(count, filters = {}) {
  let filtered = [...questionBank];
  
  if (filters.subject) {
    filtered = filtered.filter(q => q.subject === filters.subject);
  }
  if (filters.chapter) {
    filtered = filtered.filter(q => q.chapter === filters.chapter);
  }
  if (filters.topic) {
    filtered = filtered.filter(q => q.topic === filters.topic);
  }
  if (filters.difficulty) {
    filtered = filtered.filter(q => q.difficulty === filters.difficulty);
  }
  
  // Shuffle and return requested count
  const shuffled = filtered.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getAllQuestions() {
  return questionBank;
}

export function getQuestionById(id) {
  return questionBank.find(q => q.id === id);
}

// Get statistics about question coverage
export function getQuestionStats() {
  const chapterCounts = {};
  const topicCounts = {};
  
  questionBank.forEach(q => {
    chapterCounts[q.chapter] = (chapterCounts[q.chapter] || 0) + 1;
    if (q.topic) {
      topicCounts[q.topic] = (topicCounts[q.topic] || 0) + 1;
    }
  });
  
  return {
    totalQuestions: questionBank.length,
    chapterCounts,
    topicCounts,
    chaptersWithQuestions: Object.keys(chapterCounts).length,
  };
}
